-- Custom job execution history table for monitoring
CREATE TABLE IF NOT EXISTS job_execution_history (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    job_name VARCHAR(190) NOT NULL,
    job_group VARCHAR(190) NOT NULL,
    trigger_name VARCHAR(190),
    trigger_group VARCHAR(190),
    instance_name VARCHAR(190) NOT NULL,
    fired_time TIMESTAMP NOT NULL,
    completed_time TIMESTAMP NULL,
    execution_time_ms BIGINT NULL,
    status VARCHAR(20) NOT NULL,
    error_message TEXT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_job_history_job (job_name, job_group),
    INDEX idx_job_history_status (status),
    INDEX idx_job_history_fired_time (fired_time),
    INDEX idx_job_history_instance (instance_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
